"""

In python3

bytes types contain raw 8-bit values
str types contain Unicode characters


In python2

str types contain raw 8-bit values
unicode types contain Unicode characters







"""


def to_str(bytes_or_str):
    if isinstance(bytes_or_str, bytes):
        value = bytes_or_str.decode('utf-8')
    else:
        value = bytes_or_str
    return value



def to_bytes(bytes_or_str):
    if isinstance(bytes_or_str, str):
        value = bytes_or_str.encode('utf-8')
    else:
        value = bytes_or_str
    return value


    
